async function promptSqlDefault(
  rl: ReturnType<typeof createInterface>,
  f: Pick<FieldDef, "type" | "label">,
): Promise<string> {
  if (f.type === "boolean") return "0";
  for (;;) {
    const raw = await ask(
      rl,
      `Valor por defecto para las filas existentes (requerido porque "${f.label}" es obligatorio): `,
    );
    if (!raw) {
      log(
        `${c.red}Este campo es requerido: hace falta un valor por defecto para las filas ya existentes.${c.reset}`,
      );
      continue;
    }
    if (f.type === "integer") {
      if (!/^-?\d+$/.test(raw)) {
        log(`${c.red}Debe ser un número entero.${c.reset}`);
        continue;
      }
      return raw;
    }
    if (f.type === "decimal") {
      if (!/^-?\d+(\.\d+)?$/.test(raw)) {
        log(`${c.red}Debe ser un número.${c.reset}`);
        continue;
      }
      return raw;
    }
    // string / date
    return `'${raw.replace(/'/g, "''")}'`;
  }
}

