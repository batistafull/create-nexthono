function sqlDefaultLiteral(f: FieldDef): string {
  if (f.type === "boolean") return "0";
  return f.sqlDefault ?? "";
}

function genAlterMigrationSql(
  prefix: string,
  table: string,
  fields: FieldDef[],
): { filename: string; sql: string } {
  const alters = fields.map((f) => {
    let line = `ALTER TABLE ${table} ADD COLUMN ${f.name} ${SQL_TYPE[f.type]}`;
    if (f.required) line += ` NOT NULL DEFAULT ${sqlDefaultLiteral(f)}`;
    return `${line};`;
  });

  const indexBlocks = fields
    .filter((f) => f.unique)
    .map(
      (f) =>
        `\nCREATE UNIQUE INDEX IF NOT EXISTS idx_${table}_${f.name}_active\n  ON ${table} (${f.name}) WHERE deleted = 0;`,
    );

  const label = fields.length === 1 ? fields[0].name : "fields";
  return {
    filename: `${prefix}_add_${label}_to_${table}.sql`,
    sql: `-- ${prefix}_add_${label}_to_${table}: agrega ${fields.map((f) => f.name).join(", ")} a ${table}.

${alters.join("\n")}
${indexBlocks.join("\n")}
`,
  };
}

