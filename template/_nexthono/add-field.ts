function listEligibleModules(): string[] {
  if (!existsSync(BACKEND_V1_DIR)) return [];
  return readdirSync(BACKEND_V1_DIR, { withFileTypes: true })
    .filter((e) => e.isDirectory())
    .map((e) => e.name)
    .filter((table) => {
      const repoPath = join(BACKEND_V1_DIR, table, "repository.ts");
      if (!existsSync(repoPath)) return false;
      return readFileSync(repoPath, "utf8").includes("function mapRow(");
    })
    .sort();
}

function findFrontendTypeFile(pascal: string): string | null {
  if (!existsSync(SRC_TYPES_DIR)) return null;
  for (const file of readdirSync(SRC_TYPES_DIR)) {
    if (!file.endsWith(".ts")) continue;
    const p = join(SRC_TYPES_DIR, file);
    if (readFileSync(p, "utf8").includes(`export type ${pascal} = {`)) return p;
  }
  return null;
}

function loadModuleInfo(table: string): ModuleInfo {
  const backendDir = join(BACKEND_V1_DIR, table);
  const typesPath = join(backendDir, "types.ts");
  const schemaPath = join(backendDir, "schema.ts");
  const repositoryPath = join(backendDir, "repository.ts");
  const servicePath = join(backendDir, "service.ts");

  const typesContent = readFileSync(typesPath, "utf8");
  const pascalMatch = typesContent.match(/export type (\w+)Row = BaseFields/);
  if (!pascalMatch) throw new Error(`No se pudo leer el tipo de fila en ${typesPath}`);
  const Pascal = pascalMatch[1];

  const repositoryContent = readFileSync(repositoryPath, "utf8");
  const repoMatch = repositoryContent.match(/export const (\w+)Repository = \{/);
  if (!repoMatch) throw new Error(`No se pudo leer el repositorio en ${repositoryPath}`);
  const camelSingular = repoMatch[1];

  const controllerContent = readFileSync(join(backendDir, "controller.ts"), "utf8");
  const controllerMatch = controllerContent.match(/export const (\w+)Controller = \{/);
  if (!controllerMatch) throw new Error(`No se pudo leer el controller en ${backendDir}`);
  const camelPlural = controllerMatch[1];

  const frontendTypeFile = findFrontendTypeFile(Pascal);
  if (!frontendTypeFile)
    throw new Error(`No se encontró el tipo de frontend para "${Pascal}" en src/types/.`);

  const frontendServicePath = join(SRC_SERVICES_DIR, `${table}.service.ts`);
  if (!existsSync(frontendServicePath)) {
    throw new Error(`No se encontró ${frontendServicePath}`);
  }

  const frontendPagePath = join(SRC_APP_DIR, table, "page.tsx");
  if (!existsSync(frontendPagePath)) {
    throw new Error(`No se encontró ${frontendPagePath}`);
  }

  return {
    table,
    Pascal,
    camelSingular,
    camelPlural,
    paths: {
      types: typesPath,
      schema: schemaPath,
      repository: repositoryPath,
      service: servicePath,
      frontendType: frontendTypeFile,
      frontendService: frontendServicePath,
      frontendPage: frontendPagePath,
    },
  };
}

/** Field names already declared in create${Pascal}Schema (backend). */
function existingFieldNames(schemaContent: string, Pascal: string): Set<string> {
  // A module created with zero fields collapses to `z.object({});` on one
  // line — nothing to collect.
  if (schemaContent.includes(`export const create${Pascal}Schema = z.object({});`)) {
    return new Set<string>();
  }

  const startAnchor = `export const create${Pascal}Schema = z.object({\n`;
  const startIdx = schemaContent.indexOf(startAnchor);
  if (startIdx === -1) throw new Error(`No se encontró create${Pascal}Schema en schema.ts`);
  const endIdx = schemaContent.indexOf("\n});", startIdx);
  if (endIdx === -1)
    throw new Error(`No se encontró el cierre de create${Pascal}Schema en schema.ts`);
  const block = schemaContent.slice(startIdx + startAnchor.length, endIdx);
  const names = new Set<string>();
  for (const line of block.split("\n")) {
    const m = line.match(/^\s*(\w+):/);
    if (m) names.add(m[1]);
  }
  return names;
}

