function patchFrontendPageTsx(initialContent: string, Pascal: string, fields: FieldDef[]): string {
  let content = initialContent;
  content = expandIfCollapsed(content, "type FormState = {};", "type FormState = {\n\n};");
  content = expandIfCollapsed(
    content,
    "const emptyForm: FormState = {};",
    "const emptyForm: FormState = {\n\n};",
  );
  content = expandIfCollapsed(
    content,
    "    setForm({});\n    setEditingId(item.id);",
    "    setForm({\n\n    });\n    setEditingId(item.id);",
  );
  content = expandIfCollapsed(
    content,
    `      const payload = {} as Create${Pascal}Payload;`,
    `      const payload = {\n\n      } as Create${Pascal}Payload;`,
  );

  for (const f of fields) {
    content = spliceBetween(content, "type FormState = {\n", "\n};", (b) =>
      appendLine(b, `  ${f.name}: ${f.type === "boolean" ? "boolean" : "string"};`),
    );
    content = spliceBetween(content, "const emptyForm: FormState = {\n", "\n};", (b) =>
      appendLine(b, `  ${f.name}: ${f.type === "boolean" ? "false" : '""'},`),
    );
    content = spliceBetween(content, "setForm({\n", "\n    });\n    setEditingId(item.id);", (b) =>
      appendLine(b, `      ${f.name}: ${editPopulateExpr(f)},`),
    );
    content = spliceBetween(content, "const payload = {\n", "\n      } as Create", (b) =>
      appendLine(b, `        ${f.name}: ${payloadFieldExpr(f)},`),
    );
    content = insertBeforeLiteral(
      content,
      `                <th className="px-4 py-2 font-medium">Alta</th>`,
      `                <th className="px-4 py-2 font-medium">${f.label}</th>\n`,
    );
    content = insertBeforeLiteral(
      content,
      `                  <td className="px-4 py-2 text-[var(--muted-foreground)]">\n                    {formatDate(item.date_entered)}`,
      `                  <td className="px-4 py-2">{${tableCellExpr(f)}}</td>\n`,
    );
    content = insertBeforeLiteral(
      content,
      `          {formError && <p className="text-sm text-red-500 sm:col-span-2">{formError}</p>}`,
      `${formInputJsx(f)}\n`,
    );
  }

  const colSpanMatch = content.match(/colSpan=\{(\d+)\}/);
  if (!colSpanMatch) throw new Error("No se encontró colSpan={...} en page.tsx");
  const newColSpan = Number.parseInt(colSpanMatch[1], 10) + fields.length;
  content = content.replace(/colSpan=\{\d+\}/, `colSpan={${newColSpan}}`);

  return content;
}

