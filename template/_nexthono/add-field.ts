function printBanner() {
  log("");
  log(
    `${c.bold}${c.cyan}◆  nexthono-field${c.reset}${c.dim} — agrega campos a un módulo existente${c.reset}`,
  );
  log("");
}

async function main() {
  printBanner();

  const modules = listEligibleModules();
  if (modules.length === 0) {
    log(
      `${c.red}✖ No hay módulos compatibles todavía. Crea uno con "nexthono-module" primero.${c.reset}`,
    );
    process.exitCode = 1;
    return;
  }

  const rl = createInterface({ input: process.stdin, output: process.stdout });

  const choice = await askMenu(rl, "Selecciona el módulo:", modules);
  const table = modules[choice - 1];

  let info: ModuleInfo;
  try {
    info = loadModuleInfo(table);
  } catch (err) {
    log(`${c.red}✖ ${err instanceof Error ? err.message : err}${c.reset}`);
    rl.close();
    process.exitCode = 1;
    return;
  }

  const schemaContent = readFileSync(info.paths.schema, "utf8");
  const existing = existingFieldNames(schemaContent, info.Pascal);
  for (const name of BASE_FIELD_NAMES) existing.add(name);

  const fields: FieldDef[] = [];
  for (;;) {
    const addMore = await askYesNo(rl, "\n¿Desea agregar un campo adicional?");
    if (!addMore) break;

    const core = await promptFieldCore(rl, existing);

    let sqlDefault: string | undefined;
    if (core.required && core.type !== "boolean") {
      sqlDefault = await promptSqlDefault(rl, core);
      log(
        `${c.yellow}⚠ Si "${table}" ya tiene más de una fila y el campo es único, este valor repetido hará fallar el índice único al migrar.${c.reset}`,
      );
    }

    const field: FieldDef = { ...core, sqlDefault };
    existing.add(field.name);
    fields.push(field);
    log(
      `${c.green}✔ Campo "${field.name}" (${field.type}${field.required ? ", requerido" : ""}${field.unique ? ", único" : ""}) agregado.${c.reset}`,
    );
  }

  rl.close();

  if (fields.length === 0) {
    log(`${c.dim}No se agregó ningún campo.${c.reset}`);
    return;
  }

  log(`\n${c.dim}Aplicando ${fields.length} campo(s) a "${table}"…${c.reset}`);

  const originals = {
    types: readFileSync(info.paths.types, "utf8"),
    schema: readFileSync(info.paths.schema, "utf8"),
    repository: readFileSync(info.paths.repository, "utf8"),
    service: readFileSync(info.paths.service, "utf8"),
    frontendType: readFileSync(info.paths.frontendType, "utf8"),
    frontendService: readFileSync(info.paths.frontendService, "utf8"),
    frontendPage: readFileSync(info.paths.frontendPage, "utf8"),
  };

  try {
    const patched = {
      types: patchTypesTs(originals.types, info.Pascal, fields),
      schema: patchSchemaTs(originals.schema, info.Pascal, fields),
      repository: patchRepositoryTs(originals.repository, info.Pascal, table, fields),
      service: patchServiceTs(originals.service, info.camelSingular, fields),
      frontendType: patchFrontendTypeTs(originals.frontendType, info.Pascal, fields),
      frontendService: patchFrontendServiceTs(originals.frontendService, info.Pascal, fields),
      frontendPage: patchFrontendPageTsx(originals.frontendPage, info.Pascal, fields),
    };

    // Nothing is written until every patch above has succeeded.
    writeFileSync(info.paths.types, patched.types, "utf8");
    writeFileSync(info.paths.schema, patched.schema, "utf8");
    writeFileSync(info.paths.repository, patched.repository, "utf8");
    writeFileSync(info.paths.service, patched.service, "utf8");
    writeFileSync(info.paths.frontendType, patched.frontendType, "utf8");
    writeFileSync(info.paths.frontendService, patched.frontendService, "utf8");
    writeFileSync(info.paths.frontendPage, patched.frontendPage, "utf8");
  } catch (err) {
    log(
      `${c.red}✖ No se pudo parchear el módulo automáticamente: ${err instanceof Error ? err.message : err}${c.reset}`,
    );
    log(
      `${c.yellow}No se modificó ningún archivo — probablemente el módulo fue editado a mano y ya no coincide con el patrón generado. Agrega el campo manualmente.${c.reset}`,
    );
    process.exitCode = 1;
    return;
  }

  const migrationFiles = readdirSync(MIGRATIONS_DIR).filter((f) => f.endsWith(".sql"));
  const prefix = nextMigrationPrefix(migrationFiles);
  const { filename, sql } = genAlterMigrationSql(prefix, table, fields);
  const migrationPath = join(MIGRATIONS_DIR, filename);
  writeFileSync(migrationPath, sql, "utf8");

  log(`${c.green}✔ Módulo "${table}" actualizado. Migración ${filename} generada.${c.reset}`);

  formatWithBiome(projectRoot, [
    info.paths.types,
    info.paths.schema,
    info.paths.repository,
    info.paths.service,
    info.paths.frontendType,
    info.paths.frontendService,
    info.paths.frontendPage,
  ]);

  const rl2 = createInterface({ input: process.stdin, output: process.stdout });
  const runNow = await askYesNo(rl2, "\n¿Ejecutar la migración ahora (db:migrate)?");
  rl2.close();

  if (runNow) {
    const pm = detectPackageManager();
    log(`\n${c.dim}Ejecutando ${pm} run db:migrate…${c.reset}`);
    const ok = runMigration(projectRoot, pm);
    log(
      ok
        ? `${c.green}✔ Migración aplicada.${c.reset}`
        : `${c.yellow}⚠ La migración falló — revisa ${filename} y ejecútala manualmente.${c.reset}`,
    );
  }

  log(`\n${c.bold}${c.green}Listo!${c.reset} "${table}" tiene ${fields.length} campo(s) nuevo(s).`);
  if (!runNow)
    log(`  Ejecuta ${c.cyan}db:migrate${c.reset} para aplicar los cambios a la base de datos.`);
  log(`  Reinicia ${c.cyan}dev${c.reset} si estaba corriendo.\n`);
}

main().catch((err) => {
  console.error(`${c.red}✖ ${err?.message ?? err}${c.reset}`);
  process.exit(1);
});
