/**
 * Adds one or more fields to an EXISTING module generated by
 * .nexthono/create-module.ts (backend + migration + frontend), by patching the
 * already-generated files in place rather than regenerating them — so any
 * unrelated hand-edits survive. Run with:
 *
 *   npm run nexthono-field
 *
 * Only modules that look like they were produced by create-module.ts are
 * offered (detected via the `mapRow` helper in repository.ts) — the `users`
 * reference module has bespoke auth logic that doesn't match this shape, so
 * it's intentionally excluded.
 */

const __dirname = dirname(fileURLToPath(import.meta.url));
const projectRoot = resolve(__dirname, "..");

const BACKEND_V1_DIR = join(projectRoot, "server", "api", "v1");
const MIGRATIONS_DIR = join(projectRoot, "server", "api", "database", "migrations");
const SRC_TYPES_DIR = join(projectRoot, "src", "types");
const SRC_SERVICES_DIR = join(projectRoot, "src", "services");
const SRC_APP_DIR = join(projectRoot, "src", "app");

interface ModuleInfo {
  table: string;
  Pascal: string;
  camelSingular: string;
  camelPlural: string;
  paths: {
    types: string;
    schema: string;
    repository: string;
    service: string;
    frontendType: string;
    frontendService: string;
    frontendPage: string;
  };
}

